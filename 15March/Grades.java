public class Grades{
 
  public static void main(String[] args){
  
   String grade = args[0];
   switch (grade){
   
   case "A" :
     System.out.println("Perfect");
       break;
   case "B" :
     System.out.println("Very Good");
       break;
   case "C" :
     System.out.println("Good");
       break;
   case "D" :
     System.out.println("Pass");
       break;
   case "E" :
     System.out.println("Pass");
       break;
   case "F" :
     System.out.println("Fail");
       break;
 
   }

  }

}