public class SeasonPerMonth{
  public static void main(String[] args){
    
    String month = args[0];
    switch(month){
      
      case "January" : 
        System.out.println("Winter");    
          break;
      case "February" : 
         System.out.println("Winter");    
          break;
      case "March" : 
        System.out.println("Spring");    
          break;
      case "April" : 
        System.out.println("Spring");    
          break;
      case "May" : 
        System.out.println("Spring");    
          break;
      case "June" : 
        System.out.println("Summer");    
          break;
      case "July" : 
        System.out.println("Summer");    
          break;
      case "August" : 
        System.out.println("Summer");   
          break;
      case "September" : 
        System.out.println("Fall");    
          break;
     case "October" : 
        System.out.println("Fall");    
          break;
     case "November" : 
        System.out.println("Fall");    
          break;
     case "December" : 
        System.out.println("Winter");    
          break;
     }

  }
}